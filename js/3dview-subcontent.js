	'use strict';

	function ThreeDViewGridSubContent() {
		this.step = {
			t: 3600*4,
			lat: 0.05,
			lng: 0.05
		};
		
		this.origin = {
			t: 0,
			lat: 0,
			lng: 0
		};
		
		this.timeListMap = {};
		this.flattenList = [];
	}
	
	ThreeDViewGridSubContent.prototype = {
		setStep: function(lat, lng, t) {
			this.step.lat = lat;
			this.step.lng = lng;
			this.step.t   = t;
		},
		
		setOrigin: function(lat, lng, t) {
			this.origin.lat = lat;
			this.origin.lng = lng;
			this.origin.t   = t;
		},

		getLatitudeFromIndex: function(latIndex) {
			return this.origin.lat + latIndex * this.step.lat;
		},

		getLongitudeFromIndex: function(lngIndex) {
			return this.origin.lng + lngIndex * this.step.lng;
		},
		
		getTimeFromIndex: function(timeIndex) {
			return this.origin.t + timeIndex * this.step.t;
		},

		add: function(timeIndex, latIndex, lngIndex, value) {
			var timeListEntry = this.getTimeListByTimeIndex(timeIndex);
			timeListEntry.addCell(latIndex, lngIndex, value);
		},
		
		getTimeListByTimeIndex: function(ti) {
			var ent = this.timeListMap[ti];
			if (!ent) {
				var ent = new ThreeDViewGridSubContent.TimeListEntry();
				this.timeListMap[ti] = ent;
			}
			
			return ent;
		},
		
		close: function() {
			this.linkBetweenTimePeriods();
		},
		
		linkBetweenTimePeriods: function() {
			var posi_map = this.gatherPositionIndex();
			var time_list = this.makeSortedTimeIndices();

			for (var p in posi_map) {
				this.linkBetweenTimePeriodsOfPosition(time_list, p);
			}
		},
		
		linkBetweenTimePeriodsOfPosition: function(sorted_time_list, posi) {
			var prevT = null;
			for (var ti in sorted_time_list) {
				var timeIndex = sorted_time_list[ti];
				var tlEntry = this.timeListMap[timeIndex];
				if (tlEntry.xyMap.hasOwnProperty(posi)) {
					var cell = tlEntry.xyMap[posi];
					if (prevT !== null) {
						cell.prevTime = this.origin.t + this.step.t * prevT;
					} else {
						cell.prevTime = null;
					}

					prevT = timeIndex;
				}
			}
		},
		
		gatherPositionIndex: function() {
			var res = {};

			for (var ti in this.timeListMap) {
				var tl = this.timeListMap[ti];
				tl.gatherPositionIndex(res);
			}
			
			return res;
		},
		
		makeSortedTimeIndices: function() {
			var res = [];
			for (var ti in this.timeListMap) { res.push(ti - 0); }
			
			res.sort( function compareNumbers(a, b) { return a - b; } );
			return res;
		},
		
		addTestData: function() {
			addAnnotationGridTestData2(this);
		},
		
		count: function() {
			var n = 0;
			for (var i in this.timeListMap) {
				n += this.timeListMap[i].count();
			}
			
			return n;
		},
		
		forEachCell: function(proc) {
			for (var ti in this.timeListMap) {
				ti -= 0;

				var xyMap = this.timeListMap[ti].xyMap;
				var upperXY = this.timeListMap[ti+1] ? this.timeListMap[ti+1].xyMap : null;
				
				for (var posi in xyMap) {
					var exists_upper = false;
					if (upperXY) {
						exists_upper = upperXY.hasOwnProperty(posi);
					}
					
					var posElements = posi.split('_');
					if (posElements.length > 1) {
						var yk = posElements[0] - 0;
						var xk = posElements[1] - 0;

						var rightKey = makeLatLngKey(yk, xk+1);
						var backKey = makeLatLngKey(yk+1, xk);
						var ex_r = xyMap.hasOwnProperty(rightKey);
						var ex_b = xyMap.hasOwnProperty(backKey);
						
						proc(ti - 0, yk, xk, xyMap[posi],  exists_upper, ex_r, ex_b);
					}
				}
			}
		}
	};
	
	ThreeDViewGridSubContent.TimeListEntry = function() {
		this.xyMap = {};
	}
	
	ThreeDViewGridSubContent.TimeListEntry.prototype = {
		addCell: function(latIndex, lngIndex, value) {
			var key = makeLatLngKey(latIndex, lngIndex);
			this.xyMap[key] = {value: value, prevTime: null};
		},
		
		count: function() {
			var n = 0;
			for (var i in this.xyMap) {
				++n;
			}
			
			return n;
		},
		
		gatherPositionIndex: function(outMap) {
			for (var i in this.xyMap) {
				outMap[i] = 1;
			}
		}
	};
	
	function makeLatLngKey(latIdx, lngIdx) {
		return latIdx + "_" + lngIdx;
	}
	
